--CREATE TABLES
CREATE TABLE FILM (
FilmID CHAR(9) NOT NULL,
FilmType VARCHAR2(25) NOT NULL,
Title VARCHAR2(100) NOT NULL,
ReleaseYear NUMBER(4,0) NOT NULL,
RunTime NUMBER(3,0),
Rating NUMBER(3,1),
Votes NUMBER(6,0),
Budget NUMBER (10,0),
BoxOffice NUMBER (10,0),
Company VARCHAR2(100),
CONSTRAINT FILM_PK PRIMARY KEY (FilmID)
);

CREATE TABLE GENRE (
FilmID CHAR(9) NOT NULL,
Genre VARCHAR2(25) NOT NULL,
CONSTRAINT GENRE_PK PRIMARY KEY (FilmID, Genre),
CONSTRAINT GENRE_FK FOREIGN KEY (FilmID) REFERENCES FILM (FilmID)
);

CREATE TABLE PERSON (
PersonID CHAR(9) NOT NULL,
FirstName VARCHAR2(25) NOT NULL,
MiddleName VARCHAR2(25),
LastName VARCHAR2(25),
Suffix VARCHAR2(25),
BirthYear NUMBER(4,0),
DeathYear NUMBER(4,0),
CONSTRAINT PERSON_PK PRIMARY KEY (PersonID)
);

CREATE TABLE CREW (
CrewID CHAR(9) NOT NULL,
FilmID CHAR(9) NOT NULL,
PersonID CHAR(9) NOT NULL,
CrewTitle VARCHAR2(25) NOT NULL,
Character VARCHAR2(50),
CONSTRAINT CREW_PK PRIMARY KEY (CrewID),
CONSTRAINT CREW_FK1 FOREIGN KEY (FilmID) REFERENCES FILM (FilmID),
CONSTRAINT CAREW_FK2 FOREIGN KEY (PersonID) REFERENCES PERSON (PersonID)
);

CREATE TABLE AWARD (
AwardID CHAR(9) NOT NULL,
FilmID CHAR(9) NOT NULL,
Award VARCHAR2(50) NOT NULL,
Category VARCHAR2(50) NOT NULL,
Won NUMBER(1,0) CHECK (Won IN (0,1)),
CONSTRAINT AWARD_PK PRIMARY KEY (AwardID),
CONSTRAINT AWARD_FK FOREIGN KEY (FilmID) REFERENCES FILM (FilmID)
);

